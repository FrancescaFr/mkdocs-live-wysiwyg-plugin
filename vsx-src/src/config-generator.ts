// --- NEW NORMALIZER HELPER ---
// Converts any messy mix of dictionaries and arrays into a strict MkDocs array
function normalizeMkDocsList(list: unknown): Array<string | Record<string, unknown>> {
  if (!list) return [];
  const result: Array<string | Record<string, unknown>> = [];

  // Case 1: User provided a pure dictionary (No dashes in YAML)
  if (!Array.isArray(list) && typeof list === 'object') {
    for (const [key, val] of Object.entries(list)) {
      if (val !== null && typeof val === 'object') {
        result.push({ [key]: val });
      } else {
        result.push(key);
      }
    }
    return result;
  }

  // Case 2: User provided an array (Might contain giant multi-key dictionaries)
  if (Array.isArray(list)) {
    for (const item of list) {
      if (typeof item === 'string') {
        result.push(item);
      } else if (item && typeof item === 'object') {
        // Split giant dictionaries into strict single-key MkDocs items
        for (const [key, val] of Object.entries(item)) {
          if (val !== null && typeof val === 'object') {
            result.push({ [key]: val });
          } else {
            result.push({ [key]: val ?? {} });
          }
        }
      }
    }
  }

  return result;
}

// --- NEW DEEP MERGE HELPER ---
function mergeMkDocsConfig(parent: any, child: any): any {
  const merged = { ...parent, ...child };

  // Deep merge specific dictionary keys
  const dictKeys = ['theme', 'extra'];
  for (const key of dictKeys) {
    if (parent[key] && child[key] && typeof parent[key] === 'object' && typeof child[key] === 'object') {
      merged[key] = { ...parent[key], ...child[key] };
    }
  }

  // Smart Merge for collections (Allows the user's dictionary trick!)
  const hybridKeys = ['plugins', 'markdown_extensions'];
  for (const key of hybridKeys) {
    if (parent[key] && child[key]) {
      if (!Array.isArray(parent[key]) && !Array.isArray(child[key]) && typeof parent[key] === 'object' && typeof child[key] === 'object') {
         // User cleverly used dictionaries -> Deep merge them
         merged[key] = { ...parent[key], ...child[key] };
      } else {
         // Standard MkDocs arrays -> Concatenate them
         const pArr = Array.isArray(parent[key]) ? parent[key] : [parent[key]];
         const cArr = Array.isArray(child[key]) ? child[key] : [child[key]];
         merged[key] = [...pArr, ...cArr];
      }
    } else if (parent[key] && !child[key]) {
       merged[key] = parent[key];
    }
  }

  // Pure array keys
  const arrayKeys = ['extra_css', 'extra_javascript'];
  for (const key of arrayKeys) {
    if (parent[key] || child[key]) {
      const pArr = Array.isArray(parent[key]) ? parent[key] : (parent[key] ? [parent[key]] : []);
      const cArr = Array.isArray(child[key]) ? child[key] : (child[key] ? [child[key]] : []);
      merged[key] = [...pArr, ...cArr];
    }
  }

  return merged;
}

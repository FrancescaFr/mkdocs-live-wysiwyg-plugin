/**
 * Preprocess YAML text to escape ALL tags that js-yaml cannot handle
 */
export const YAML_TAG_RE = /!!python\/([a-zA-Z0-9_./:-]+)/g;
export const YAML_TAG_SENTINEL = '__PYTAG__';
export const YAML_SENTINEL_RE = new RegExp(`${YAML_TAG_SENTINEL}([a-zA-Z0-9_./:-]+)`, 'g');

export function preprocessYaml(raw: string): string {
  return raw.replace(YAML_TAG_RE, `${YAML_TAG_SENTINEL}$1`);
}

export function postprocessYaml(dumped: string): string {
  return dumped.replace(YAML_SENTINEL_RE, '!!python/$1');
}

interface MkDocsConfig {
  plugins?: Array<string | Record<string, unknown>>;
  theme?: string | Record<string, unknown>;
  markdown_extensions?: Array<string | Record<string, unknown>>;
  hooks?: string[];
  [key: string]: unknown;
}

function getPluginName(plugin: string | Record<string, unknown>): string {
  if (typeof plugin === 'string') { return plugin; }
  return Object.keys(plugin)[0] ?? '';
}

function extractUserPlugins(config: MkDocsConfig): Array<string | Record<string, unknown>> {
  if (!config.plugins) { return []; }

  // We now guarantee this is a perfectly flat array of strings and single-key objects!
  return (config.plugins as Array<string | Record<string, unknown>>).filter((p) => {
    const name = getPluginName(p);
    return !['live-edit', 'live-wysiwyg', 'techdocs-core', 'glightbox'].includes(name);
  });
}

function buildPluginsBlock(
  themeName: string,
  docsDir: string,
  websocketPort: number,
  apiPort: number,
  autoGenerated: boolean,
  bindHost: string
): Array<string | Record<string, unknown>> {
  const plugins: Array<string | Record<string, unknown>> = ['search'];

  // if (themeName === 'material') {
  //   plugins.push({
  //     'techdocs-core': {
  //       use_material_search: true,
  //       use_pymdownx_blocks: true,
  //     },
  //   });
  // }

  if (autoGenerated) {
    plugins.push('mkdocs-nav-weight');
  }

  plugins.push({
    'live-edit': {
      user_docs_dir: docsDir,
      websockets_port: websocketPort,
      websockets_host: bindHost,
    },
  });

  const wysiwygConfig: Record<string, unknown> = {};
  if (apiPort > 0) {
    wysiwygConfig.api_port = apiPort;
  }
  plugins.push(
    Object.keys(wysiwygConfig).length > 0
      ? { 'live-wysiwyg': wysiwygConfig }
      : 'live-wysiwyg'
  );

  return plugins;
}

function resolveThemeName(config: MkDocsConfig, useUserTheme: boolean): string {
  if (!useUserTheme) { return 'material'; }
  const theme = config.theme;
  if (typeof theme === 'string') { return theme; }
  if (theme && typeof theme === 'object' && 'name' in theme) {
    return String(theme.name);
  }
  return 'material';
}

function applyTheme(rendered: MkDocsConfig, useUserTheme: boolean, themeName: string): void {
  if (useUserTheme) {
    // Let MkDocs natively handle the theme!
    // Just ensure it's formatted safely for the YAML dumper.
    if (typeof rendered.theme === 'string') {
      rendered.theme = { name: rendered.theme };
    }
  } else {
    rendered.theme = {
      name: 'material',
      palette: DEFAULT_PALETTE,
    };
  }
}

function addMarkdownExtensionIfMissing(config: MkDocsConfig, ext: string): void {
  if (!config.markdown_extensions) {
    config.markdown_extensions = [];
  }
  const existing = config.markdown_extensions.some((e) => {
    const name = typeof e === 'string' ? e : Object.keys(e)[0];
    return name === ext;
  });
  if (!existing) {
    config.markdown_extensions.push(ext);
  }
}

function addSuperfencesWithMermaidIfMissing(config: MkDocsConfig): void {
  if (!config.markdown_extensions) {
    config.markdown_extensions = [];
  }
  const hasSuperfences = config.markdown_extensions.some((e) => {
    const name = typeof e === 'string' ? e : Object.keys(e)[0];
    return name === 'pymdownx.superfences';
  });
  if (!hasSuperfences) {
    config.markdown_extensions.push(MERMAID_SUPERFENCES_CONFIG);
  }
}

function generateRestoreThemeHook(
  workspaceDir: string,
  themeConfig: Record<string, unknown>, // Keep this for future-proofing
  websocketPort: number,
  externalWsUrl: string
): string {
  const vscodeDir = path.join(workspaceDir, '.vscode');
  if (!fs.existsSync(vscodeDir)) {
    fs.mkdirSync(vscodeDir, { recursive: true });
  }

  const hookPath = path.join(vscodeDir, 'restore_theme.py');

  // We write the hook to ONLY handle the WebSocket redirection.
  // This allows the MkDocs 'INHERIT' logic to handle 100% of the theme/plugins.
  fs.writeFileSync(hookPath, `
def on_post_page(output, page, config, **kwargs):
    # This patch ensures the WYSIWYG editor can talk to the server
    # even through VS Code / Codespaces proxies.
    patch = """<script>
    (function() {
        const origWs = window.WebSocket;
        window.WebSocket = function(url, protocols) {
            if (url.includes(':${websocketPort}')) {
                return new origWs('${externalWsUrl}', protocols);
            }
            return new origWs(url, protocols);
        };
    })();
    </script>"""
    return output.replace('</head>', patch + '</head>')
`);

  return hookPath;
}


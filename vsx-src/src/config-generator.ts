export function generateMkdocsConfig(options: GenerateConfigOptions): string {
  const {
    workspaceDir,
    tmpDir,
    websocketPort,
    apiPort,
    httpPort,
    bindHost,
    externalWsUrl,
    useUserTheme,
    autoGenerated,
  } = options;

  const originalConfigPath = path.join(workspaceDir, 'mkdocs.yml');
  const generatedConfigPath = originalConfigPath;

  const originalContent = fs.readFileSync(originalConfigPath, 'utf8');

  // Backup original configuration to tmpDir so it can be restored later
  fs.mkdirSync(tmpDir, { recursive: true });
  fs.writeFileSync(path.join(tmpDir, 'original-mkdocs.yml'), originalContent);

  const sanitized = preprocessYaml(originalContent);
  let config = (yaml.load(sanitized) as any) || {};

  // --- FLATTEN INHERITANCE ---
  if (config.INHERIT) {
    const inheritedPath = path.resolve(path.dirname(originalConfigPath), String(config.INHERIT));
    if (fs.existsSync(inheritedPath)) {
      try {
        const parentRaw = fs.readFileSync(inheritedPath, 'utf8');
        const parentContent = preprocessYaml(parentRaw);
        const parentConfig = (yaml.load(parentContent) as any) || {};

        // Merge the parent into the child using our helper
        config = mergeMkDocsConfig(parentConfig, config);

        // CRITICAL: Delete INHERIT so the proxy file doesn't trigger pathing errors!
        delete config.INHERIT;
      } catch (err) {
        config.INHERIT_ERROR = `Failed to parse inherited file. ${err}`;
      }
    } else {
      config.INHERIT_ERROR = `Inherited file not found at path: ${inheritedPath}`;
    }
  }

  // --- APPLY NORMALIZER ---
  // Instantly fix any weird formatting from the inheritance merge (like the giant dict)
  config.plugins = normalizeMkDocsList(config.plugins);
  config.markdown_extensions = normalizeMkDocsList(config.markdown_extensions);

  // --- PREPARE PLUGINS & THEME ---
  const userPlugins = extractUserPlugins(config);
  const themeName = resolveThemeName(config, useUserTheme);
  const docsDir = path.join(workspaceDir, 'docs');

  const systemPlugins = buildPluginsBlock(
    themeName,
    docsDir,
    websocketPort,
    apiPort,
    autoGenerated,
    bindHost
  );

  // --- BUILD FINAL CONFIGURATION ---
  const rendered: MkDocsConfig = { ...config };

  // Bind the main server
  rendered.dev_addr = `${bindHost}:${httpPort}`;

  // Deduplicate plugins (Prevents the 'material/search' warning)
  const allPlugins = [...userPlugins, ...systemPlugins];
  const uniquePlugins: Array<string | Record<string, unknown>> = [];
  const seenPlugins = new Set<string>();

  for (const p of allPlugins) {
    const name = getPluginName(p);
    if (!seenPlugins.has(name)) {
      seenPlugins.add(name);
      uniquePlugins.push(p);
    }
  }
  rendered.plugins = uniquePlugins;

  applyTheme(rendered, useUserTheme, themeName);

  // Generate hook for websockets
  const themeObj = (typeof rendered.theme === 'object' ? rendered.theme : {}) as Record<string, unknown>;
  const hookPath = generateRestoreThemeHook(workspaceDir, themeObj, websocketPort, externalWsUrl);
  rendered.hooks = [...(rendered.hooks ?? []), hookPath];

  // Add required Markdown extensions for WYSIWYG
  for (const ext of REQUIRED_MARKDOWN_EXTENSIONS) {
    addMarkdownExtensionIfMissing(rendered, ext);
  }
  addSuperfencesWithMermaidIfMissing(rendered);

  // --- TRANSPARENCY / DEBUGGING HOOK ---
  const debugPath = path.join(workspaceDir, 'DEBUG_MKDOCS_PROXY.json');
  fs.writeFileSync(debugPath, JSON.stringify(rendered, null, 2));

  // CRITICAL: Clean up our custom debug keys before passing to MkDocs so it doesn't throw warnings!
  delete rendered.INHERIT_ERROR;

  const banner = [
    '################################################################################',
    '# DO NOT EDIT THIS FILE while the server is running.',
    '#',
    '# WARNING: This entire mkdocs.yml file will be deleted. The original will be',
    '# restored overwriting this file when the server shuts down.',
    '#',
    '# If you want to edit your mkdocs.yml, do so after shutting down the server.',
    '################################################################################',
    '',
  ].join(os.EOL);

  // Dump and post-process to restore !!python tags
  const rawYaml = yaml.dump(rendered, {
    lineWidth: -1,
    noRefs: true,
    quotingType: '"',
    forceQuotes: false,
  });

  const yamlContent = postprocessYaml(rawYaml);
  const generatedContent = banner + yamlContent;

  fs.writeFileSync(generatedConfigPath, generatedContent);

  return generatedConfigPath;
}

